const CurrencySelect = ({ selectedCurrency, handleCurrency }) => {
    const [isOpen, setIsOpen] = useState(false);
    const [search, setSearch] = useState("");
    const [filteredCurrencies, setFilteredCurrencies] = useState(currencyCodes);
    const [notFound, setNotFound] = useState(false);
    const dropdownRef = useRef(null);

    const handleToggleDropdown = () => {
        setIsOpen(!isOpen);
    };

    const handleSelectCurrency = (currency) => {
        handleCurrency({ target: { value: currency } });
        setSearch(""); // Clear the search input when a currency is selected
        setIsOpen(false);
    };

    const handleClickOutside = (e) => {
        if (dropdownRef.current && !dropdownRef.current.contains(e.target)) {
            setIsOpen(false);
        }
    };

    const filterCurrencies = (searchTerm) => {
        const filtered = currencyCodes.filter(currency => 
            currency.toLowerCase().includes(searchTerm.toLowerCase()) || 
            currencyNames[currency].toLowerCase().includes(searchTerm.toLowerCase())
        );
        setFilteredCurrencies(filtered);
        setNotFound(filtered.length === 0);
    };

    useEffect(() => {
        filterCurrencies(search);
    }, [search]);

    useEffect(() => {
        if (isOpen) {
            document.addEventListener("mousedown", handleClickOutside);
            // Automatically focus on the search input when the dropdown opens
            if (dropdownRef.current) {
                const input = dropdownRef.current.querySelector(".currency-select-search");
                if (input) {
                    input.focus();
                }
            }
        } else {
            document.removeEventListener("mousedown", handleClickOutside);
        }
        return () => document.removeEventListener("mousedown", handleClickOutside);
    }, [isOpen]);

    return (
        <div className="currency-select" ref={dropdownRef}>
            <div className="currency-select-header" onClick={handleToggleDropdown}>
                <img src={`https://flagsapi.com/${selectedCurrency.substring(0, 2)}/flat/64.png`} alt="Flag" />
                <span>{selectedCurrency} - {currencyNames[selectedCurrency]}</span>
                <svg width="16" height="16" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg">
                    <path d="M12 15.5L8.5 12H15.5L12 15.5Z" fill="#fff"/>
                </svg>
            </div>
            {isOpen && (
                <div className="currency-select-dropdown">
                    <input
                        type="text"
                        className="currency-select-search"
                        placeholder="Search for a currency by code or name..."
                        value={search}
                        onChange={(e) => setSearch(e.target.value)}
                    />
                    {notFound ? (
                        <p className="currency-select-not-found">No results found</p>
                    ) : (
                        <ul className="currency-select-options">
                            {filteredCurrencies.map(currency => (
                                <li
                                    key={currency}
                                    className="currency-select-option"
                                    onClick={() => handleSelectCurrency(currency)}
                                >
                                    {currency} - {currencyNames[currency]}
                                </li>
                            ))}
                        </ul>
                    )}
                </div>
            )}
        </div>
    );
};

export default CurrencySelect;